<?php
function validateDate($date) {
    $pattern = '/^(0[1-9]|[12][0-9]|3[01])\.(0[1-9]|1[0-2])\.(19|20)\d{2}$/';
    return preg_match($pattern, $date);
}

$dates = [
    '31.12.2020', // верно
    '29.02.2020', // верно (високосный год)
    '31.04.2021', // неверно (в апреле 30 дней)
    '12.13.2022', // неверно (несуществующий месяц)
    '01.01.99'    // неверно (должен быть 1999)
];

foreach ($dates as $date) {
    echo "$date - " . (validateDate($date) ? "верно" : "неверно") . "\n";
}
?>